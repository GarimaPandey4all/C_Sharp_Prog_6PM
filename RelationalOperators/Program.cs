using System;

namespace RelationalOperators
{
    class Program
    {
        static void Main(string[] args)
        {
            // Relational or Comparison
            int a = 10, b = 30;

            Console.WriteLine(a == b);
            Console.WriteLine(a != b);
            Console.WriteLine(a > b);
            Console.WriteLine(a < b);
            Console.WriteLine(a >= b);
            Console.WriteLine(a <= b);
        }
    }
}
